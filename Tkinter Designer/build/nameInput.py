from pathlib import Path
from tkinter import Tk, Canvas, Entry, Text, Button, PhotoImage
ASSETS_PATH = Path(f"{Path(__file__).parent}/assets/nameInput")
def relative_to_assets(path: str) -> Path:return ASSETS_PATH / Path(path)
window = Tk()
window.geometry("600x600")
window.configure(bg = "#FFFFFF")
canvas = Canvas(window,bg = "#FFFFFF",height = 600,width = 600,bd = 0,highlightthickness = 0,relief = "ridge")
canvas.place(x = 0, y = 0)
image_image_1 = PhotoImage(file=relative_to_assets("image_1.png"))
image_1 = canvas.create_image(300,300,image=image_image_1)
entry_image_1 = PhotoImage(file=relative_to_assets("entry_1.png"))
entry_bg_1 = canvas.create_image(300,300,image=entry_image_1)
entry_1 = Entry(bd=0,bg="#D9D9D9",fg="#000716",highlightthickness=0)
entry_1.place(x=110,y=291,width=380,height=30)
canvas.create_text(114,108,anchor="nw",text="What is your name?",fill="#FFFFFF",font=("Inter", 40 * -1))
canvas.create_text(105,278,anchor="nw",text="Name",fill="#000000",font=("Inter Regular", 13 * -1))
window.resizable(False, False)
window.mainloop()
